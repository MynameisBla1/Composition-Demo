public class Resolution {
    //this resolution class, composes monitor, and it only has two fields the width and height of the monitor.
    private int width;
    private int height;

    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
